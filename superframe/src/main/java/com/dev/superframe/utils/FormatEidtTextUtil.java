    //手机号码的格式(中间4位变*)
    public static void phoneNumAddAsterisk(final EditText mEditText) {
        mEditText.addTextChangedListener(new TextWatcher() {
            String strAsterisk = "";
            int beforeTextLength = 0;
            int onTextLength = 0;
            boolean isChanged = false;

            int location = 0;// 记录光标的位置
            private char[] tempChar;
            private StringBuffer buffer = new StringBuffer();
            int konggeNumberB = 0;

            @Override
            public void beforeTextChanged(CharSequence s, int start, int count, int after) {
                beforeTextLength = s.length();
                if (buffer.length() > 0) {
                    buffer.delete(0, buffer.length());
                }
                konggeNumberB = 0;
                for (int i = 0; i < s.length(); i++) {
                    if (s.charAt(i) == ' ') {
                        konggeNumberB++;
                    }
                }
            }

            @Override
            public void onTextChanged(CharSequence s, int start, int before, int count) {
                onTextLength = s.length();
                buffer.append(s.toString());
                if (onTextLength == beforeTextLength || onTextLength <= 3 || isChanged) {
                    isChanged = false;
                    return;
                }
                isChanged = true;
            }

            @Override
            public void afterTextChanged(Editable s) {
                String text = s.toString();
                if (text.length() < 4) {
                    strAsterisk = "";
                } else if (text.length() == 4) {
                    strAsterisk += s.toString().substring(3, text.length());
                } else if (text.length() == 5) {
                    strAsterisk += s.toString().substring(4, text.length());
                } else if (text.length() == 6) {
                    strAsterisk += s.toString().substring(5, text.length());
                } else if (text.length() == 7) {
                    strAsterisk += s.toString().substring(6, text.length());
                }
                if (isChanged) {
                    Log.e("FormatEidtTextUtil", "strAsterisk:" + strAsterisk);
                    location = mEditText.getSelectionEnd();
                    int index = 0;
                    while (index < buffer.length()) {
                        if (buffer.charAt(index) == ' ') {
                            buffer.deleteCharAt(index);
                            strAsterisk = "";
                        } else {
                            index++;
                        }
                    }

                    index = 0;
                    int konggeNumberC = 0;
                    while (index < buffer.length()) {
                        if ((index == 3 || index == 4 || index == 5 || index == 6)) {
                            buffer.replace(index, index + 1, "*");
                            konggeNumberC++;
                        }
                        index++;
                    }

                    if (konggeNumberC > konggeNumberB) {
                        location += (konggeNumberC - konggeNumberB);
                    }

                    tempChar = new char[buffer.length()];
                    buffer.getChars(0, buffer.length(), tempChar, 0);
                    String str = buffer.toString();
                    if (location > str.length()) {
                        location = str.length();
                    } else if (location < 0) {
                        location = 0;
                    }

                    mEditText.setText(str);
                    mEditText.setTag(str.replace("****", strAsterisk).replace("*", ""));
                    Log.e("FormatEidtTextUtil", "EditText.setText:" + str);
                    Log.e("FormatEidtTextUtil", "EditText.setTag:" + str.replace("****", strAsterisk).replace("*", ""));
                    Editable etable = mEditText.getText();
                    Selection.setSelection(etable, location);
                    isChanged = false;
                }
            }
        });
    }
